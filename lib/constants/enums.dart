/// 按钮类型枚举 - button type enum
enum AntdButtonType {
  /// 默认
  normal,

  /// 主题样式
  primary,

  /// 超链接样式
  link,

  /// 文本样式
  text,
}

/// 按钮大小，默认：32
enum AntdButtonSize {
  /// 大按钮，高度为：40
  larger,

  /// 中按钮，高度为：36。默认
  middle,

  /// 小按钮，高度为：28
  small,
}

/// 按钮形状
enum AntdButtonShape {
  /// 默认
  normal,

  /// 圆形
  circle,

  /// 圆角形
  round,
}

/// 文本类型
enum AntdTypofraphyType {
  secondary,
  success,
  warning,
  danger,
}

/// 标题重要程度
enum AntdTitleLevel {
  h1,
  h2,
  h3,
  h4,
  h5,
}

/// 分割线位置
enum AntdDividerOrientation {
  left,
  center,
  right,
}

/// 分割线类型
enum AntdDividerType {
  horizontal,
  vertical,
}

/// 间距大小
enum AntdSpaceSize {
  small,
  middle,
  larger,
}

enum AntdSpaceAlign {
  start,
  end,
  center,
  baseline,
}

enum AntdSpaceDirection {
  vertical,
  horizontal,
}

enum AntdSiderTheme {
  light,
  dark,
}

enum AntdCardSize {
  normal,
  small,
}

// 主题类型
enum AntdThemeType {
  success,
  info,
  warning,
  error,
}
